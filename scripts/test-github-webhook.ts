/**
 * Smoke-test for the GitHub webhook handler.
 * Generates HMAC-signed payloads for each supported event type and POSTs them
 * to the local or remote /api/github/webhooks endpoint.
 *
 * Usage:
 *   GITHUB_WEBHOOK_SECRET=<secret> pnpm tsx scripts/test-github-webhook.ts
 *   WEBHOOK_URL=https://paperclip.rk9.fi/api/github/webhooks GITHUB_WEBHOOK_SECRET=<secret> pnpm tsx scripts/test-github-webhook.ts
 *
 * Optional flags:
 *   --repo=<owner/name>   Override repository.full_name in payloads (default: mv50000/alli-audit)
 *   --only=<event>        Only run scenarios for this x-github-event header
 */

import { createHmac, randomUUID } from "node:crypto";

const WEBHOOK_URL = process.env.WEBHOOK_URL ?? "http://localhost:3100/api/github/webhooks";
const SECRET = process.env.GITHUB_WEBHOOK_SECRET ?? "";

interface Args {
  repo: string;
  only: string | null;
}

function parseArgs(argv: string[]): Args {
  const out: Args = { repo: "mv50000/alli-audit", only: null };
  for (const arg of argv) {
    if (arg.startsWith("--repo=")) out.repo = arg.slice("--repo=".length);
    else if (arg.startsWith("--only=")) out.only = arg.slice("--only=".length);
    else if (arg === "--help" || arg === "-h") {
      console.log("Usage: pnpm tsx scripts/test-github-webhook.ts [--repo=<owner/name>] [--only=<event>]");
      process.exit(0);
    }
  }
  return out;
}

function sign(body: string): string {
  return "sha256=" + createHmac("sha256", SECRET).update(body).digest("hex");
}

interface Scenario {
  event: string;
  description: string;
  expectsDispatch: boolean;
  payload: () => Record<string, unknown>;
}

