interface Result {
  scenario: Scenario;
  status: number;
  body: Record<string, unknown> | null;
  error?: string;
}

async function send(scenario: Scenario): Promise<Result> {
  const body = JSON.stringify(scenario.payload());
  const signature = sign(body);
  const delivery = randomUUID();
  try {
    const res = await fetch(WEBHOOK_URL, {
      method: "POST",
      headers: {
        "content-type": "application/json",
        "x-hub-signature-256": signature,
        "x-github-event": scenario.event,
        "x-github-delivery": delivery,
        "user-agent": "paperclip-smoke/1.0",
      },
      body,
    });
    let parsed: Record<string, unknown> | null = null;
    try {
      parsed = (await res.json()) as Record<string, unknown>;
    } catch {
      parsed = null;
    }
    return { scenario, status: res.status, body: parsed };
  } catch (err) {
    return {
      scenario,
      status: 0,
      body: null,
      error: err instanceof Error ? err.message : String(err),
    };
  }
}

function fmtRow(label: string, status: number, slackDispatched: number | string, expectsDispatch: boolean, ok: boolean): string {
  const prefix = ok ? "✓" : "✗";
  return `${prefix}  ${String(status).padEnd(4)} dispatched=${String(slackDispatched).padEnd(3)} expected=${expectsDispatch ? "yes" : "no "}  ${label}`;
}

async function main() {
  if (!SECRET) {
    console.error("ERROR: GITHUB_WEBHOOK_SECRET environment variable is required");
    process.exit(2);
  }
  const args = parseArgs(process.argv.slice(2));
  const scenarios = buildScenarios(args.repo).filter((s) => !args.only || s.event === args.only);
  if (scenarios.length === 0) {
    console.error(`No scenarios match filter: ${args.only}`);
    process.exit(2);
  }
  console.log(`POST ${WEBHOOK_URL} (${scenarios.length} scenarios, repo=${args.repo})`);

  let pass = 0;
  let fail = 0;
  for (const scenario of scenarios) {
    const result = await send(scenario);
    if (result.error) {
      fail++;
      console.log(`✗  ERR  ${scenario.event}: ${result.error}`);
      continue;
    }
    const dispatched =
      typeof result.body?.slackDispatched === "number" ? (result.body.slackDispatched as number) : "?";
    const ok = result.status === 200;
    if (ok) pass++;
    else fail++;
    const label = `${scenario.event} — ${scenario.description}`;
    console.log(fmtRow(label, result.status, dispatched, scenario.expectsDispatch, ok));
  }
  console.log(`\n${pass} ok, ${fail} fail`);
  process.exit(fail === 0 ? 0 : 1);
}

main().catch((err) => {
  console.error(err);
  process.exit(2);
});
